import streamlit as st

# Ρυθμίσεις Παγκόσμιου Επιπέδου
st.set_page_config(page_title="ΓΕΩΠΡΟΝΟΙΑ PRO", page_icon="🧪", layout="wide")

st.title("🌿 ΓΕΩΠΡΟΝΟΙΑ: Ψηφιακή Γεωπονική Αυθεντία")
st.markdown("---")

# Πλευρική στήλη για ρυθμίσεις
st.sidebar.header("📍 Παράμετροι Πεδίου")
crop = st.sidebar.selectbox("Καλλιέργεια στόχος:", ["Ελιά", "Αμπέλι", "Πατάτα", "Φράουλα", "Καρπούζι", "Πεπόνι"])
method = st.sidebar.radio("Σύστημα Καλλιέργειας:", ["Ξερική", "Ποτιστική"])

st.write(f"### Ανάλυση Δεδομένων για: **{crop} ({method})**")
st.info("Εισάγετε τις τιμές της εργαστηριακής ανάλυσης για την εξαγωγή επιστημονικού πορίσματος.")

# Οργάνωση εισαγωγής δεδομένων σε στήλες
col1, col2, col3 = st.columns(3)

with col1:
    ph = st.number_input("pH (Οξύτητα)", value=7.0, step=0.1, help="Διεθνές πρότυπο 1:1 H2O")
    om = st.number_input("Οργανική Ουσία (%)", value=1.5, step=0.1)
    nitrate = st.number_input("Άζωτο (N-NO3) ppm", value=20.0)

with col2:
    potassium = st.number_input("Κάλιο (K) ppm", value=150.0)
    magnesium = st.number_input("Μαγνήσιο (Mg) ppm", value=200.0)
    phosphorus = st.number_input("Φώσφορος (P) ppm", value=15.0)

with col3:
    boron = st.number_input("Βόριο (B) ppm", value=0.5, step=0.1)
    calcium = st.number_input("Ασβέστιο (Ca) meq/100g", value=10.0)
    salinity = st.number_input("Αγωγιμότητα (EC) mS/cm", value=0.4)

if st.button("🚀 ΕΚΤΕΛΕΣΗ ΕΠΙΣΤΗΜΟΝΙΚΗΣ ΑΝΑΛΥΣΗΣ"):
    st.markdown("---")
    st.header("📋 Επιστημονική Αξιολόγηση & Στρατηγική")
    
    # 1. Υπολογισμός Κρίσιμων Σχέσεων (Ratios) - Εδώ είναι η επιστήμη!
    k_mg_ratio = potassium / (magnesium + 0.1)
    
    # Δημιουργία Συμβουλών
    recommendations = []
    
    # ΓΕΝΙΚΟΙ ΚΑΝΟΝΕΣ (Βάσει FAO)
    if om < 1.5:
        recommendations.append("❌ **ΚΡΙΣΙΜΟ: Οργανικό Απόθεμα.** Η τιμή είναι κάτω από το όριο ασφαλείας. Η ικανότητα ανταλλαγής κατιόντων (CEC) είναι περιορισμένη. Προτεραιότητα: Ενσωμάτωση χουμικών οξέων ή κομπόστ.")
    
    if salinity > 1.2 and crop in ["Φράουλα", "Πεπόνι"]:
        recommendations.append("⚠️ **ΑΛΑΤΟΤΗΤΑ:** Υψηλός κίνδυνος ωσμωτικού στρες. Αποφύγετε λιπάσματα με υψηλό δείκτη αλατότητας (π.χ. Χλωριούχο Κάλιο).")

    # ΕΞΕΙΔΙΚΕΥΣΗ ΑΝΑ ΚΑΛΛΙΕΡΓΕΙΑ
    if crop == "Ελιά":
        if boron < 0.6:
            recommendations.append("🍈 **ΕΛΙΑ - ΒΟΡΙΟ:** Η τιμή είναι οριακή για ανθοφορία. Διεθνείς μελέτες δείχνουν ότι για επιτυχή καρπόδεση απαιτείται διαφυλλικός ψεκασμός με Βόριο στο στάδιο της 'λευκής μπαλίτσας'.")
        if method == "Ξερική" and potassium < 180:
            recommendations.append("💧 **ΕΛΙΑ - ΞΕΡΙΚΗ:** Σε συνθήκες έλλειψης νερού, η ρίζα αδυνατεί να προσλάβει Κάλιο. Προτείνεται αποκλειστικά διαφυλλική ενίσχυση με Θειικό Κάλιο μετά την πήξη του πυρήνα.")

    elif crop == "Πατάτα":
        if ph > 7.2:
            recommendations.append("🥔 **ΠΑΤΑΤΑ - pH:** Κίνδυνος Ακτινομύκωσης (Scab). Απαιτείται οξίνιση της ριζόσφαιρας με χρήση στοιχειακού θείου ή λιπασμάτων με βάση την αμμωνία.")
        if k_mg_ratio < 0.5:
            recommendations.append("✨ **ΠΑΤΑΤΑ - ΠΟΙΟΤΗΤΑ:** Η σχέση K/Mg είναι υπέρ του Μαγνησίου. Αυτό θα μειώσει το ειδικό βάρος της πατάτας. Χρειάζεται άμεση ενίσχυση με Κάλιο για να 'γεμίσει' ο κόνδυλος.")

    elif crop == "Αμπέλι":
        if k_mg_ratio > 0.8:
             recommendations.append("🍇 **ΑΜΠΕΛΙ - ΙΣΟΡΡΟΠΙΑ:** Πολύ υψηλό Κάλιο σε σχέση με το Μαγνήσιο. Κίνδυνος εμφάνισης 'ξήρανσης ράχης'. Μειώστε την καλιούχο λίπανση και ενισχύστε με Μαγνήσιο.")

    elif crop in ["Καρπούζι", "Πεπόνι"]:
        if calcium < 8:
            recommendations.append("🍉 **ΚΑΡΠΟΥΖΙ/ΠΕΠΟΝΙ:** Χαμηλό ενεργό ασβέστιο. Κίνδυνος για σχίσιμο καρπών και 'τάπα' (Blossom end rot). Χρειάζεται νιτρικό ασβέστιο μέσω υδρολίπανσης.")

    # Εμφάνιση Αποτελεσμάτων
    for rec in recommendations:
        st.write(rec)

    st.markdown("---")
    st.success("Η ανάλυση ολοκληρώθηκε. Η 'ΓΕΩΠΡΟΝΟΙΑ' χρησιμοποιεί αλγόριθμους που βασίζονται στην παγκόσμια γεωπονική βιβλιογραφία για τη μεγιστοποίηση της απόδοσης και την προστασία του εδάφους.")
