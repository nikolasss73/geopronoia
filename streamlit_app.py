import streamlit as st

# Ρυθμίσεις σελίδας
st.set_page_config(page_title="ΓΕΩΠΡΟΝΟΙΑ", page_icon="🌿")

# Τίτλος και Καλωσόρισμα
st.title("🌿 ΓΕΩΠΡΟΝΟΙΑ")
st.subheader("Ο δικός σας ανεξάρτητος σύμβουλος για το χωράφι")
st.write("Βασισμένο σε διεθνείς γεωπονικές μελέτες")

# Εισαγωγή Στοιχείων
st.sidebar.header("Στοιχεία Καλλιέργειας")
crop = st.sidebar.selectbox("Τι καλλιεργείτε;", ["Ελιά", "Αμπέλι", "Πατάτα", "Φράουλα", "Καρπούζι", "Πεπόνι"])
method = st.sidebar.radio("Τύπος καλλιέργειας:", ["Ξερική", "Ποτιστική"])

st.write("---")
st.write("### Συμπληρώστε τις τιμές από την ανάλυση εδάφους:")

# Κουτάκια για τα νούμερα
col1, col2 = st.columns(2)
with col1:
    ph = st.number_input("pH εδάφους (π.χ. 6.17)", min_value=0.0, max_value=14.0, value=7.0, step=0.01)
    om = st.number_input("Οργανική Ουσία % (π.χ. 0.90)", min_value=0.0, max_value=10.0, value=1.0, step=0.01)
with col2:
    boron = st.number_input("Βόριο (B) σε ppm (π.χ. 0.24)", min_value=0.0, max_value=10.0, value=0.5, step=0.01)
    nitrogen = st.number_input("Άζωτο Νιτρικό σε ppm (π.χ. 24.7)", min_value=0.0, max_value=100.0, value=20.0, step=0.1)

# Κουμπί για το αποτέλεσμα
if st.button("Λήψη Συμβουλής"):
    st.write("---")
    st.header("📋 Το Πόρισμα της ΓΕΩΠΡΟΝΟΙΑΣ")
    
    # Λογική Συμβουλών
    advice = []
    
    if om < 1.0:
        advice.append("⚠️ **ΟΡΓΑΝΙΚΗ ΟΥΣΙΑ:** Είναι πολύ χαμηλή. Το χώμα σας δεν μπορεί να κρατήσει τα λιπάσματα. Πριν ξοδέψετε χρήματα, ρίξτε κοπριά ή οργανικά υλικά.")
    
    if ph > 7.5:
        advice.append("🧴 **ΥΨΗΛΟ pH:** Το φυτό δυσκολεύεται να απορροφήσει σίδηρο και ιχνοστοιχεία. Μην ρίχνετε σίδηρο στο χώμα, προτιμήστε διαφυλλικούς ψεκασμούς.")
    elif ph < 6.0:
        advice.append("🧪 **ΧΑΜΗΛΟ pH:** Το έδαφος είναι όξινο. Χρειάζεται προσθήκη ασβέστη για να έρθει σε ισορροπία.")

    if boron < 0.4:
        advice.append("🌸 **ΕΛΛΕΙΨΗ ΒΟΡΙΟΥ:** Προσοχή! Θα έχετε πρόβλημα στο δέσιμο του καρπού. Χρειάζεται διαφυλλικός ψεκασμός με Βόριο πριν την άνθηση.")

    if nitrogen > 30:
        advice.append("🚫 **ΠΟΛΥ ΑΖΩΤΟ:** Έχετε ήδη αρκετό άζωτο. Μην ρίξετε άλλο τώρα, θα κάψετε τα φυτά και θα πετάξετε χρήματα.")

    for item in advice:
        st.write(item)
    
    st.info("Η ΓΕΩΠΡΟΝΟΙΑ σας συμβουλεύει με βάση την επιστήμη. Εμπιστευτείτε το χωράφι σας, όχι τον έμπορο!")

st.write("---")
st.caption("Δημιουργήθηκε για την στήριξη του Έλληνα αγρότη.")
