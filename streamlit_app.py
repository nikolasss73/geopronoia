import streamlit as st

# Ρυθμίσεις Παγκόσμιας Αυθεντίας
st.set_page_config(page_title="ΓΕΩΠΡΟΝΟΙΑ ULTIMATE", page_icon="🔬", layout="wide")

# Διόρθωση στο στυλ της σελίδας
st.markdown("""
    <style>
    .main { background-color: #f5f7f9; }
    .stButton>button { width: 100%; background-color: #2e7d32; color: white; height: 3em; font-size: 20px; }
    </style>
    """, unsafe_allow_html=True)

st.title("🌿 ΓΕΩΠΡΟΝΟΙΑ: Παγκόσμιο Σύστημα Λήψης Αποφάσεων")
st.write("---")

# ΠΛΕΥΡΙΚΗ ΣΤΗΛΗ - ΕΠΙΛΟΓΕΣ
st.sidebar.header("⚙️ Ρυθμίσεις Καλλιέργειας")
crop = st.sidebar.selectbox("Επιλέξτε Καλλιέργεια:", ["Ελιά", "Αμπέλι", "Πατάτα", "Φράουλα", "Καρπούζι", "Πεπόνι"])
method = st.sidebar.radio("Σύστημα Διαχείρισης:", ["Ξερική (Χωρίς Πότισμα)", "Ποτιστική (Με Υδρολίπανση)"])

st.sidebar.markdown("---")
st.sidebar.write("⚠️ **Οδηγία:** Οι τιμές που εισάγετε πρέπει να προέρχονται από επίσημο εδαφολογικό εργαστήριο.")

# ΚΥΡΙΟ ΜΕΝΟΥ ΕΙΣΑΓΩΓΗΣ
st.header(f"📊 Δεδομένα Ανάλυσης για {crop}")
col1, col2, col3 = st.columns(3)

with col1:
    ph = st.number_input("pH (Οξύτητα)", value=6.17, help="Ιδανικό για τα περισσότερα: 6.5-7.5")
    om = st.number_input("Οργανική Ουσία (%)", value=0.90)
    nitrogen = st.number_input("Άζωτο (N) ppm", value=24.0)

with col2:
    potassium = st.number_input("Κάλιο (K) ppm", value=150.0)
    phosphorus = st.number_input("Φώσφορος (P) ppm", value=15.0)
    magnesium = st.number_input("Μαγνήσιο (Mg) ppm", value=200.0)

with col3:
    boron = st.number_input("Βόριο (B) ppm", value=0.24)
    calcium = st.number_input("Ασβέστιο (Ca) meq/100g", value=6.0)
    ec = st.number_input("Αγωγιμότητα (EC) mS/cm", value=0.35)

if st.button("🔍 ΕΚΤΕΛΕΣΗ ΒΑΘΙΑΣ ΑΝΑΛΥΣΗΣ"):
    st.markdown("---")
    
    # ΤΕΧΝΙΚΟ ΠΟΡΙΣΜΑ
    st.header("🔬 Επιστημονικό Πόρισμα & Στρατηγική")
    
    # Λογική Συστήματος (Expert System)
    st.subheader("📍 Κατάσταση Εδάφους")
    if om < 1.2:
        st.error(f"🔴 **ΚΡΙΣΙΜΟ:** Η Οργανική Ουσία ({om}%) είναι πολύ χαμηλή. Το έδαφος δεν έχει 'μνήμη'. Οποιαδήποτε λίπανση θα έχει απώλειες άνω του 40%. **ΔΡΑΣΗ:** Άμεση προσθήκη χουμικών οξέων και οργανικής ύλης (κοπριά ή κομπόστ).")
    
    if ph < 6.0:
        st.warning("🟠 **pH:** Το έδαφος τείνει να γίνει όξινο. Θα έχετε δέσμευση Φωσφόρου. Χρειάζεται ασβέστωση.")
    elif ph > 7.8:
        st.warning("🟠 **pH:** Υψηλή αλκαλικότητα. Τα ιχνοστοιχεία (Σίδηρος, Ψευδάργυρος) είναι 'κλειδωμένα'.")

    # Εξειδικευμένες Συμβουλές
    st.subheader(f"🌱 Στρατηγική για {crop}")
    
    if crop == "Ελιά":
        if boron < 0.5:
            st.info("💡 **ΒΟΡΙΟ:** Είσαι σε έλλειψη. Στην ελιά αυτό σημαίνει ακαρπία. **Ψεκασμός:** 2 διαφυλλικοί ψεκασμοί με Βόριο, ένας στην έκπτυξη των οφθαλμών (μούρλα) και ένας πριν την άνθηση.")
        if "Ξερική" in method:
            st.write("💧 **ΞΕΡΙΚΗ ΔΙΑΧΕΙΡΙΣΗ:** Μην ρίξετε κοκκώδες λίπασμα μετά τον Μάρτιο. Μεταβείτε σε διαφυλλικά σκευάσματα Καλίου το καλοκαίρι.")
        else:
            st.write("🚿 **ΥΔΡΟΛΙΠΑΝΣΗ:** Χρησιμοποιήστε Νιτρικό Κάλιο στο στάδιο της σκλήρυνσης του πυρήνα.")

    elif crop == "Πατάτα":
        if ph > 7.0:
            st.warning("🥔 **ΠΑΤΑΤΑ:** Υψηλό pH. Προσοχή στην ακτινομύκωση. Χρησιμοποιήστε όξινα λιπάσματα.")
        st.write("🎯 **ΣΤΡΑΤΗΓΙΚΗ:** Χρειάζεστε Κάλιο (K) σε αναλογία 1:2 με το Άζωτο για καλό 'γέμισμα'.")

    elif crop == "Αμπέλι":
        k_mg_ratio = potassium / (magnesium + 0.1)
        if k_mg_ratio > 0.8:
            st.warning("🍇 **ΣΧΕΣΗ K/Mg:** Πολύ υψηλό Κάλιο. Κίνδυνος για 'ξήρανση ράχης'.")
        st.write("🍷 **ΠΟΙΟΤΗΤΑ:** Διαφυλλικό Κάλιο και Βόριο 20 μέρες πριν τον τρύγο για σάκχαρα.")

    elif crop in ["Καρπούζι", "Πεπόνι", "Φράουλα"]:
        if calcium < 8:
            st.error(f"🍉 **ΑΣΒΕΣΤΙΟ:** Πολύ χαμηλό ({calcium}). Κίνδυνος για σκίσιμο καρπών.")
        st.write("🍓 **ΣΥΜΒΟΥΛΗ:** Νιτρικό Ασβέστιο μέσω υδρολίπανσης από τη στιγμή που θα δέσει ο καρπός.")

    st.markdown("---")
    st.caption("ΓΕΩΠΡΟΝΟΙΑ: Επιστημονική καθοδήγηση βάσει παγκόσμιας βιβλιογραφίας.")
